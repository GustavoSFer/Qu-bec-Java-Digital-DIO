public class Calculadora {
  public static void soma(int valorA, int valorB) {
    System.out.println("A soma é: " + (valorA + valorB));
  }
  public static void subtracao(int valorA, int valorB) {
    System.out.println("A subtração é: " + (valorA - valorB));
  }
  public static void multiplicacao(int valorA, int valorB) {
    System.out.println("A multiplicacao é: " + (valorA * valorB));
  }
  public static void divisão(float valorA, float valorB) {
    System.out.println("A divisão é: " + (valorA / valorB));
  }
}
